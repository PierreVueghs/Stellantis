InputDataForExport
LoadTestData::GetIndexForInputData(const std::string &theInputDataName) const {
  if (theInputDataName == "id")
    return IDFE_id;
  else if (theInputDataName == "Timestamp")
    return IDFE_Timestamp;
  else if (theInputDataName == "State")
    return IDFE_State;
  else if (theInputDataName == "LoD")
    return IDFE_LoD;
  else
    return IDFE_UNKNOWN;
}

