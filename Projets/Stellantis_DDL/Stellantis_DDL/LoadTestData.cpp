bool LoadTestData::LoadCsvFile() {
  FILE *file;
  errno_t error;
  error = fopen_s(&file, myFileName.c_str(), "r");

  if (!file) {
    printf("Could not open the file\n");
    return false;
  }

  myTestData = myModel->InitializeTestData();

  char content[4096];
  char *next_token;

  fgets(content, 4096, file);
  char *v = strtok_s(content, ",", &next_token);
  std::map<size_t, size_t> aMapOfIndices;
  size_t anIndex(0);
  while (v) {
    ++anIndex;
    std::string aString(v);

    size_t anIndexZozo(0);

    int aTestDataIndex = myModel->GetIndexForPredictorName(aString);
    if (aTestDataIndex >= 0) {
      aMapOfIndices.emplace(anIndex, static_cast<size_t>(aTestDataIndex));
    }
    v = strtok_s(NULL, ",", &next_token);
  }

  size_t aCounter(0);
  while (fgets(content, 4096, file)) {
    ++aCounter;
    std::string aStringContent(content);
    std::istringstream aStringStream(content);
    std::string aValue;

    for (size_t aCellIndex(0); aCellIndex < anIndex; ++aCellIndex) {
      std::getline(aStringStream, aValue, ',');
      const auto &it = aMapOfIndices.find(aCellIndex);
      if (it != end(aMapOfIndices)) {
        myTestData.at(it->second)
            .push_back(std::make_pair(aCounter, std::stod(aValue.c_str())));
      }
    }
  }
  fclose(file);
  myNumberOfTestData = aCounter;
  return true;
}

bool LoadTestData::ReLoadCsvFile() {
  FILE *file;
  errno_t error;
  error = fopen_s(&file, myFileName.c_str(), "r");

  if (!file) {
    printf("Could not open the file\n");
    return false;
  }

  char content[4096];
  char *next_token;

  fgets(content, 4096, file);
  char *v = strtok_s(content, ",", &next_token);
  std::map<size_t, InputDataForExport> aMapOfIndices;
  size_t anIndex(1);
  while (v) {
    ++anIndex;
    std::string aString(v);

    size_t anIndexZozo(0);

    InputDataForExport anInputDataIndex = GetIndexForInputData(aString);
    if (anInputDataIndex != IDFE_UNKNOWN)
      aMapOfIndices.emplace(anIndex, anInputDataIndex);
    v = strtok_s(NULL, ",", &next_token);
  }

  size_t aCounter(0);
  while (fgets(content, 4096, file)) {
    ++aCounter;
    char *v = strtok_s(content, ",", &next_token);
    size_t anIndex(0);
    while (v) {
      ++anIndex;
      std::string aValue(v);
      const auto &it = aMapOfIndices.find(anIndex);
      if (it != end(aMapOfIndices)) {
        myInputValuesForExport.at(it->second).push_back(aValue);
      }
      v = strtok_s(NULL, ",", &next_token);
    }
  }
  fclose(file);
  return true;
}

